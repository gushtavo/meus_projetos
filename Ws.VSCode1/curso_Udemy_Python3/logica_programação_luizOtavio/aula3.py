'''
python = linguagem de programoção
Tipo de  tipagem = Dinamica / forte
str -> string -> texto
Strings sao textos que estao dentro de aspas
'''

# aspas simples
print('Gustavo roberto')
print(1, 'Gustavo "roberto"')
# aspas duplas
print("Laura")
print(2, "'Laura'")

# escape
print('Gustavo \'roberto')

# r
print(r'Gustavo \'roberto\'')
